package whileDowhile;

public class WhileLoop {

	public static void main(String[] args) {
		
		int i = 20;//9 8 7 
		while(i<25) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("================");
		int j = 20;
		do {
			System.out.println(j);
			j++; // j = 21
		}while(j<25);
		

	}

}
